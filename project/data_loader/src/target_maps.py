import numpy as np
from skimage import draw
from matplotlib import pyplot as plt

def magnitude_map(depth_image: np.ndarray, edges):
    """
    Create a magnitude map for the given points
    :param depth_image:  depth image
    :param edges:       list of 2d points
    :return:    magnitude map of same shape as depth image
    """
    line_map = np.ones(depth_image.shape)
    for edge in edges:
        start, end = edge[0], edge[1]
        rr, cc = draw.line(start[1], start[0], end[1], end[0])
        rr_s, rr_e = constrain_interval(rr, line_map.shape[0])
        cc_s, cc_e = constrain_interval(cc, line_map.shape[1])
        start = max(rr_s, cc_s)
        end = min(rr_e, cc_e)

        if end != -1:
            rr = rr[start:end]
            cc = cc[start:end]
            line_map[rr, cc] = 0

    return line_map


def constrain_interval(arr, max_val):
    idx = 0
    while (arr[idx] < 0 or arr[idx] > max_val-1) and idx < len(arr)-1:
        idx += 1
    if idx >= len(arr):
        idx = -1
    arr_start = idx

    idx = len(arr) - 1
    while (arr[idx] < 0 or arr[idx] > max_val-1) and idx > 0:
        idx -= 1
    if idx < 0:
        idx = -1
    arr_end = idx

    return arr_start, arr_end


